using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Charlotte.Tools
{
	public class D4Color
	{
		public double R;
		public double G;
		public double B;
		public double A;

		public D4Color()
		{ }

		public D4Color(double r, double g, double b, double a)
		{
			this.R = r;
			this.G = g;
			this.B = b;
			this.A = a;
		}
	}
}
